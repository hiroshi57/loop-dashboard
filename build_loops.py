    pains = []
    for t in arch["tags"]:
        for p in TAG_TO_PAIN.get(t, []):
            if p not in pains:
                pains.append(p)
    # 規制業種カテゴリは compliance を補強（業種側で付与）
    if arch["cat"] == "品質・コンプラ" and "compliance" not in pains:
        pains.append("compliance")
    if not pains:
        pains = ["no-strategy"]
    return pains

