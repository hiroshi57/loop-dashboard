def main():
    loops = []
    for svc_key in SVC_KEYS:
        for arch in ARCH.get(svc_key, []):
            seen_ind = []
            for ind_key in arch["inds"]:
                if ind_key in seen_ind:   # 適合業種セットの重複を無視（bug安全弁）
                    continue
                seen_ind.append(ind_key)
                loops.append(make_loop(arch, svc_key, ind_key))

    # 集計
    svc_count = Counter(l["serviceKey"] for l in loops)
    ind_count = Counter(l["industryKey"] for l in loops)
    pain_count = Counter(p for l in loops for p in l["painPoints"])
    services = [{"key": k, "name": n, "description": d, "loopCount": svc_count.get(k, 0)} for (k, n, d) in SERVICES]
    industries = [{"key": k, "name": n, "description": d, "loopCount": ind_count.get(k, 0)} for (k, n, d) in INDUSTRIES]
    pains = [{"key": k, "name": n, "description": d, "loopCount": pain_count.get(k, 0)} for (k, n, d) in PAINS]

    data = {
        "meta": {
            "name": "DI Loop Library",
            "description": "loops.elorm.xyz のキックオフプロンプト形式を踏襲し、株式会社デジタルアイデンティティの13サービス×10業種に分類した外販品質のエージェントループ集。各ループは業種ごとに目標・初手・KPI・ガードレールを実態に合わせて設計している。",
            "totalLoops": len(loops),
            "generatedAt": "2026-06-13",
            "sources": ["https://digitalidentity.co.jp/service/", "github.com/hiroshi57/di-kb（PUBLICレベル情報のみ）"],
            "sourceFormat": "https://loops.elorm.xyz/loops",
            "confidentiality": "PUBLICレベルのみ。クライアント名×金額・未公開情報は非掲載。数値はすべて目標値（例）として扱う。",
            "axes": ["service", "industry", "pain"],
        },
        "services": services,
        "industries": industries,
        "pains": pains,
        "categories": CATEGORIES,
        "triggers": ["manual", "interval"],
        "loops": loops,
    }

    # 検査
    errors = []
    required = ["id", "title", "serviceKey", "industryKey", "category", "trigger", "goal",
                "context", "checkCommand", "exitCondition", "step1", "kickoffPrompt",
                "strategy", "kbfKsf", "asisToBe", "triggerDetail", "memory", "escalation"]
    for l in loops:
        for f in required:
            if not l.get(f):
                errors.append(f"空フィールド {f} @ {l.get('id')}")
        for lf in ("deliverables", "kpis", "guardrails", "tags", "tactics"):
            if not l.get(lf):
                errors.append(f"空リスト {lf} @ {l.get('id')}")
        # 6つのコアモジュールの網羅検査（記事準拠）
        cm = l.get("coreModules") or []
        cm_keys = [m.get("key") for m in cm]
        if cm_keys != EXPECTED_CORE_MODULE_KEYS:
            errors.append(f"コアモジュール不備 {cm_keys} @ {l.get('id')}")
        for m in cm:
            if not m.get("name") or not m.get("role") or not m.get("desc"):
                errors.append(f"空コアモジュール {m.get('key')} @ {l.get('id')}")
        # プレースホルダ残り検出
        for f in ("goal", "checkCommand", "exitCondition", "step1", "kickoffPrompt",
                  "strategy", "kbfKsf", "asisToBe"):
            if "{" in l[f] and "}" in l[f]:
                errors.append(f"未置換プレースホルダ {f} @ {l['id']}")
    dup_ids = [i for i, c in Counter(l["id"] for l in loops).items() if c > 1]
    if dup_ids:
        errors.append(f"重複id {len(dup_ids)}件: {dup_ids[:5]}")
    dup_titles = [t for t, c in Counter(l["title"] for l in loops).items() if c > 1]
    if dup_titles:
        errors.append(f"重複title {len(dup_titles)}件: {dup_titles[:5]}")

    OUT.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"出力: {OUT}  ({OUT.stat().st_size/1024:.0f} KB)")
    print(f"総ループ数（固有）: {len(loops)}")
    print(f"原型数: {sum(len(v) for v in ARCH.values())}")
    print("\n[サービス別 loopCount]")
    for k, n, _ in SERVICES:
        print(f"  {SVC_SHORT[k]:<12} {svc_count.get(k,0):>4}")
    print("\n[業種別 loopCount]")
    for k, n, _ in INDUSTRIES:
        print(f"  {IND_SHORT[k]:<10} {ind_count.get(k,0):>4}")
    print("\n[課題別 loopCount（複数提案の母数）]")
    for k, n, _ in PAINS:
        print(f"  {n:<22} {pain_count.get(k,0):>4}")
    cells = set((l["serviceKey"], l["industryKey"]) for l in loops)
